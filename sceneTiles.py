class sceneTiles(arcade.SpriteList):

    def setupSceneOne(self):

        self.allSpritesList = arcade.SpriteList()
        self.levelOneList = arcade.SpriteList()


        leftPos = 0
        bottomPos = 0
        for row in levelOneTiles:
            for counter in range(0, len(row)):
                if row[counter] > 0:
                    if row[counter] == 1:
                        bar = arcade.Sprite("Sprite\stoneFloor.png", SCALING)
                    elif row[counter] == 2:
                        bar = arcade.Sprite("Sprite\stoneFloorPath.png", SCALING)
                    bar.bottom = bottomPos
                    bar.left = leftPos
                    self.levelOneList.append(bar)
                    self.allSpritesList.append(bar)

                else:
                    bar = arcade.Sprite("Sprite\stoneFloor.png", SCALING)
                leftPos = leftPos + bar.width
            bottomPos = bottomPos + bar.height
            leftPos = 0
